public class AD_06_ForLoop {
    public static void main(String[] args) {
        for (int i = 0; i < 5; i++) {
            System.out.println(i);
        }
        // for each loop
        String[] God = {"Ram", "Bharat", "Laxman", "Satrugan"};
        for(String i:God)
        {
            System.out.println(i);
        }
    }

    
}
